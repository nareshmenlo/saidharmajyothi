On 29-11-2015 Sunday, <br>Venue: Brahamanandareddy stadium, Guntur.</h3>
<p>A cordial invitation to Sai Temple organizers,members of satsang, preachers, Sai tattvasevasamstha and to every adherent devotee of Sai Baba. Sai Bahugalaaarthi is a congregation of Sai devotees.</p>
<p>Karthikamasam is one of the most auspicious and sacred month which usually falls in the month of November. One can attain the heights of sanctity by worshipping lord Shiva and Damodara in this month which is also considered highly auspicious. It is considered very important to perform activities like Bathing in river, deepadhanam , visiting holy places and shrines, lighting lamp with cow’s ghee, samaradhana, reading sacred texts, Divakainkaryam.</p>
<p>Any austerity performed during the month of Karthikamasam would help one to free oneself from all the and lead them towards Moksha(liberation of soul).By doing these one can attain vaikuntaprapti and will be completely free from the karmic bondage. There is no discrimination as both rich and poor or anyone doing these good activities will be benefited. On the occasion of 105th aarthimahotsavam Sri shirdi Sai BhahugalaLakshadeepaMaha Sandhya Aarthi will be conducted to lord Shirdi Sai who is considered to be the manifestation of many deities. All the donors will be blessed for their assistance and they can attain good results.</p>
